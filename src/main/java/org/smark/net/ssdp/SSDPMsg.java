package org.smark.net.ssdp;

public class SSDPMsg {
	private String content;

	
	public SSDPMsg() {
		super();
	}


	public SSDPMsg(String content) {
		super();
		this.content = content;
	}


	public String getContent() {
		return content;
	}


	public void setContent(String content) {
		this.content = content;
	}
	
}
